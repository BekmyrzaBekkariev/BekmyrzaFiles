# Дебилный калькулятор  V1


what = input( "Что делаем? (+, -, *, /): ")

a = float( input("Ведите первое число:") )
b = float( input("Ведите второе число:") )

if what == "+":
    c = a + b
    print("Результат:" + str(c))
elif what == "-":
    c = a - b
    print("Результат:" + str(c))
elif what == "*":
    c = a * b
    print("Результат:" + str(c))
elif what == "/":
    c = a / b
    print("Результат:" + str(c))
else:
	print("Выброна не верное операция!")